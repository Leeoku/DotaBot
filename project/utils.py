def draw_digit(im, digit, x, y, c):
    pixels = digits[digit]
    x_0 = x
    for row in reversed(pixels):
        x = x_0
        for value in reversed(row):
            if value == 1:
                im[y, x] = c
            x -= 1
        y -= 1
    return (x, y)

